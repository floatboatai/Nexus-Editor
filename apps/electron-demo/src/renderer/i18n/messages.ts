function mergeMessages(partial: Partial<Record<MessageId, string>>): Record<MessageId, string> {
  const out = { ...en } as Record<MessageId, string>;
  for (const k of Object.keys(partial) as MessageId[]) {
    const v = partial[k];
    if (v !== undefined) out[k] = v;
  }
  return out;
}

/** Pick a message table from a BCP 47 tag (from settings / navigator). */
export function resolveMessages(tag: string): Record<MessageId, string> {
  const raw = tag.toLowerCase().trim();
  if (raw.startsWith("zh-tw") || raw.startsWith("zh-hk") || raw === "zh-mo") return mergeMessages(zhTW);
  if (raw.startsWith("zh")) return mergeMessages(zhCN);
  if (raw.startsWith("ja")) return mergeMessages(ja);
  if (raw.startsWith("ko")) return mergeMessages(ko);
  if (raw.startsWith("fr")) return mergeMessages(fr);
  if (raw.startsWith("de")) return mergeMessages(de);
  if (raw.startsWith("es")) return mergeMessages(es);
  return { ...en } as Record<MessageId, string>;
}
