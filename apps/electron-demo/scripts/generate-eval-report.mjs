#!/usr/bin/env node
/**
 * generate-eval-report.mjs
 *
 * Reads the Playwright JSON report produced by the CI stability gate and
 * writes a Markdown eval summary to $GITHUB_STEP_SUMMARY so the result is
 * visible directly on the Actions run page — no external dashboard needed.
 *
 * Each scenario is listed with its pass rate across repeated runs and average
 * duration. A scenario is marked stable only when every repeat passes with no
 * retries; partial failures surface as flaky signals rather than being silently
 * absorbed by the retry mechanism.
 *
 * Usage (local):  node scripts/generate-eval-report.mjs
 * Usage (CI):     automatically run after `pnpm test:e2e:stability`
 */

import { readFileSync, appendFileSync } from "node:fs";
import { resolve, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const REPORT_PATH = resolve(__dirname, "../test-results/e2e-results.json");
const SUMMARY_FILE = process.env.GITHUB_STEP_SUMMARY;

// ── Load report ───────────────────────────────────────────────────────────────

let report;
try {
  report = JSON.parse(readFileSync(REPORT_PATH, "utf8"));
} catch {
  // Report is only emitted in CI (see playwright.config.ts reporter setting).
  // In local runs without CI=true the file won't exist — exit cleanly.
  console.warn(`[eval-report] ${REPORT_PATH} not found — skipping summary`);
  process.exit(0);
}

// ── Collect specs recursively from nested suites ──────────────────────────────

function collectSpecs(suite) {
  const specs = [...(suite.specs ?? [])];
  for (const child of suite.suites ?? []) specs.push(...collectSpecs(child));
  return specs;
}

const specs = report.suites.flatMap(collectSpecs);

// ── Aggregate by title ────────────────────────────────────────────────────────
// With --repeat-each=N the same title appears N times. We group them so each
// row in the report represents one logical scenario with N sample runs.

const byTitle = new Map();
for (const spec of specs) {
  const { title } = spec;
  if (!byTitle.has(title)) byTitle.set(title, []);
  for (const test of spec.tests ?? []) {
    const last = test.results.at(-1);
    if (last) byTitle.get(title).push(last);
  }
}

// ── Build table rows ──────────────────────────────────────────────────────────

const rows = [];
for (const [title, results] of byTitle) {
  const passed = results.filter((r) => r.status === "passed").length;
  const total = results.length;
  const avgMs = Math.round(
    results.reduce((sum, r) => sum + (r.duration ?? 0), 0) / total,
  );
  const icon = passed === total ? "✅" : "❌";
  rows.push(`| ${icon} | ${title} | ${passed}/${total} | ${avgMs} ms |`);
}

// ── Build summary ─────────────────────────────────────────────────────────────

const { expected, unexpected, flaky, duration } = report.stats;
const grandTotal = expected + unexpected;
const passRate =
  grandTotal > 0 ? ((expected / grandTotal) * 100).toFixed(1) : "N/A";

const md = [
  "## 🎭 E2E Eval Report",
  "",
  "> **Stability gate** — each scenario run **3× with `--retries=0`**.",
  "> A scenario is stable only when every repeat passes independently.",
  "> Retries are disabled so flaky failures surface here rather than reaching release.",
  "",
  "| Metric | Value |",
  "|---|---|",
  `| Total runs | ${grandTotal} |`,
  `| Pass rate | ${passRate}% |`,
  `| Retry-classified flaky | ${flaky} |`,
  `| Wall time | ${(duration / 1000).toFixed(1)} s |`,
  "",
  "| Status | Scenario | Pass rate | Avg duration |",
  "|---|---|---|---|",
  ...rows,
  "",
  `_Generated by \`scripts/generate-eval-report.mjs\` · ${new Date().toISOString()}_`,
].join("\n");

process.stdout.write(md + "\n");

if (SUMMARY_FILE) {
  appendFileSync(SUMMARY_FILE, "\n" + md + "\n");
  process.stderr.write("[eval-report] Written to $GITHUB_STEP_SUMMARY ✓\n");
}
